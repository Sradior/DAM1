    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here
        String cambios = "";  // String para representar el siguiente texto // "" para que se quede vacío
        
        try{                  // Try + Catch para controlar una excepción que se genera a través del siguiente código
            File otroarchivo = new File ("agenda.txt"); // File para obtener información 
            Scanner lector = new Scanner(otroarchivo);
            while(lector.hasNextLine()){ 
              cambios += lector.nextLine()+"\n"; // el "while" proporciona el permiso de ejecución de esta acción sobre una condición
            
            } 
        }catch(IOException e){ // "IO" = input output
            e.printStackTrace(); // sería para imprimir el registro
        }
        cambios = cambios.replace("|", " ");
        jTextArea1.setText(cambios);
    }//GEN-LAST:event_jButton2ActionPerformed

